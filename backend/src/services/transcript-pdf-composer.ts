type PdfKitDoc = InstanceType<typeof PDFDocument>;

function drawLetterhead(
  doc: PdfKitDoc,
  ctx: ComposeTranscriptContext,
  tz: string,
): void {
  const clinicLine = ctx.practiceName?.trim() || "Clariva Clinic";
  doc
    .font('Helvetica-Bold')
    .fontSize(16)
    .fillColor('#000')
    .text(clinicLine, { align: 'left' });

  doc
    .font('Helvetica')
    .fontSize(10)
    .fillColor('#444')
    .text(
      `Dr. ${ctx.doctorDisplayName}${
        ctx.doctorSpecialty ? `, ${ctx.doctorSpecialty}` : ''
      }`,
      { align: 'left' },
    );

  doc.moveDown(0.5);
  doc
    .font('Helvetica-Bold')
    .fontSize(13)
    .fillColor('#000')
    .text('Consultation transcript', { align: 'left' });
  doc.moveDown(0.2);

  doc
    .font('Helvetica')
    .fontSize(10)
    .fillColor('#000');
  const dateLabel = formatDateLabel(ctx.consultEndedAtIso, tz);
  doc.text(`Patient: ${ctx.patientDisplayName || '—'}`);
  doc.text(`Date:    ${dateLabel}`);
  doc.text(`Modality: ${ctx.modality}`);
  doc.text(`Session ID: ${truncateSessionId(ctx.sessionId)}...`);

  doc.moveDown(0.8);
  doc
    .strokeColor('#bbb')
    .lineWidth(0.5)
    .moveTo(doc.page.margins.left, doc.y)
    .lineTo(doc.page.width - doc.page.margins.right, doc.y)
    .stroke();
  doc.moveDown(0.5);
}

function drawTranscriptionPendingBanner(doc: PdfKitDoc): void {
  doc
    .font('Helvetica-Bold')
    .fontSize(10)
    .fillColor('#a60')
    .text('── Audio transcription pending ──', { align: 'center' })
    .moveDown(0.2)
    .font('Helvetica')
    .fillColor('#666')
    .text(
      'The spoken portion of this consult is still being transcribed. ' +
        'Please re-download in a few minutes for the complete transcript.',
      { align: 'center' },
    )
    .moveDown(0.8)
    .fillColor('#000');
}

function drawBody(
  doc: PdfKitDoc,
  merged: MergedItem[],
  ctx: ComposeTranscriptContext,
  tz: string,
): void {
  doc.font('Helvetica').fontSize(10).fillColor('#000');
  for (const item of merged) {
    drawRow(doc, item, ctx, tz);
    doc.moveDown(0.15);
  }
}

function drawRow(
  doc: PdfKitDoc,
  item: MergedItem,
  ctx: ComposeTranscriptContext,
  tz: string,
): void {
  if (item.source === 'voice') {
    const hhmm = formatTimeHHMM(item.timestampIso, tz);
    doc
      .font('Helvetica')
      .fillColor('#000')
      .text(`${hhmm}  🎙 ${item.speakerLabel}: ${item.text}`, { align: 'left' });
    return;
  }
  const hhmm = formatTimeHHMM(item.createdAtIso, tz);
  if (item.kind === 'system') {
    doc
      .font('Helvetica-Oblique')
      .fillColor('#555')
      .text(`${hhmm}  [System: ${item.body}]`, { align: 'left' })
      .font('Helvetica')
      .fillColor('#000');
    return;
  }
  const senderLabel = resolveChatSenderLabel(item.senderRole, ctx);
  if (item.kind === 'attachment') {
    const filename = extractFilename(item.attachmentUrl);
    const body =
      `${hhmm}  ${senderLabel}: [attached: ${item.attachmentMime} — ${filename}]` +
      (item.body ? ` — ${item.body}` : '');
    doc.font('Helvetica').fillColor('#000').text(body, { align: 'left' });
    return;
  }
  // kind === 'text'
  doc
    .font('Helvetica')
    .fillColor('#000')
    .text(`${hhmm}  ${senderLabel}: ${item.body}`, { align: 'left' });
}

function drawFooter(
  doc: PdfKitDoc,
  opts: { practiceName?: string; generatedAt: Date },
): void {
  const pageBottom = doc.page.height - doc.page.margins.bottom + 8;
  const leftX = doc.page.margins.left;
  const rightX = doc.page.width - doc.page.margins.right;

  doc
    .strokeColor('#bbb')
    .lineWidth(0.5)
    .moveTo(leftX, pageBottom - 4)
    .lineTo(rightX, pageBottom - 4)
    .stroke();

  const clinic = opts.practiceName?.trim() || 'your clinic';
  const genLabel = opts.generatedAt.toISOString();

  // `lineBreak: false` + explicit `height` keeps pdfkit from treating a
  // footer overrun as "time to paginate" — this is the fix for the
  // infinite-recursion bug we hit in the first cut where footer writes
  // kept triggering new pages.
  const footerTextOpts = {
    width:      rightX - leftX,
    align:      'center' as const,
    lineBreak:  false,
    height:     12,
  };

  doc
    .font('Helvetica')
    .fontSize(8)
    .fillColor('#777')
    .text(WATERMARK_TEXT, leftX, pageBottom, footerTextOpts)
    .text(
      `This transcript was generated by Clariva on ${genLabel}. For the authoritative audio recording, contact ${clinic}.`,
      leftX,
      pageBottom + 12,
      footerTextOpts,
    )
    .text(SIGNATURE_TEXT, leftX, pageBottom + 36, footerTextOpts);
}

// ============================================================================
// Internals — formatting
// ============================================================================

function formatTimeHHMM(iso: string, tz: string): string {
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return '--:--';
  try {
    return new Intl.DateTimeFormat('en-GB', {
      timeZone: tz,
      hour:    '2-digit',
      minute:  '2-digit',
      hour12:  false,
    }).format(d);
  } catch {
    return new Intl.DateTimeFormat('en-GB', {
      timeZone: DEFAULT_TZ,
      hour:    '2-digit',
      minute:  '2-digit',
      hour12:  false,
    }).format(d);
  }
}

function formatDateLabel(iso: string, tz: string): string {
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return '—';
  try {
    return new Intl.DateTimeFormat('en-GB', {
      timeZone: tz,
      day:   '2-digit',
      month: 'short',
      year:  'numeric',
    }).format(d);
  } catch {
    return new Intl.DateTimeFormat('en-GB', {
      timeZone: DEFAULT_TZ,
      day:   '2-digit',
      month: 'short',
      year:  'numeric',
    }).format(d);
  }
}

function truncateSessionId(sessionId: string): string {
  const trimmed = (sessionId ?? '').trim();
  if (trimmed.length <= 8) return trimmed;
  return trimmed.slice(0, 8);
}

function extractFilename(url: string): string {
  const trimmed = (url ?? '').trim();
  if (!trimmed) return 'file';
  const noQuery = trimmed.split('?')[0];
  const segments = noQuery.split('/');
  const last = segments[segments.length - 1] ?? '';
  return last || 'file';
}

function resolveChatSenderLabel(
  role: 'doctor' | 'patient' | 'system',
  ctx: ComposeTranscriptContext,
): string {
  switch (role) {
    case 'doctor': {
      const name = ctx.doctorDisplayName?.trim();
      return name ? `Dr. ${name}` : 'Doctor';
    }
    case 'patient':
      return 'Patient';
    case 'system':
      // System rows are rendered via the `[System: ...]` branch; this
      // label is a defensive fallback only.
      return 'System';
  }
}
